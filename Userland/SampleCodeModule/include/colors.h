/**
 * @file colors.h
 * @brief Definition of color constants for the operating system.
 */

#ifndef COLORS_H
#define COLORS_H

#define GREY    0x00F0F0F0  /**< Grey color */
#define RED     0x00FF0000  /**< Red color */
#define GREEN   0x0000FF00  /**< Green color */
#define WHITE   0x00FFFFFF  /**< White color */
#define BLACK   0x00000000  /**< Black color */
#define BLUE    0x000000FF  /**< Blue color */
#define YELLOW  0x00FFFF00  /**< Yellow color */
#define LOWKEY  0x00787878  /**< Lowkey color */
#define CYAN    0x0007B6D9  /**< Cyan color */

#define DEFAULT GREY        /**< Default color */

#endif